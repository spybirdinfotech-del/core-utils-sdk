import 'package:flutter/material.dart';

class CustomButtonTheme {
  final double? height;
  final double? width;
  final Color? buttonColor;
  final TextStyle? buttonTextStyle;
  final BorderRadiusGeometry? borderRadius;

  const CustomButtonTheme({
    this.height,
    this.width,
    this.buttonColor,
    this.buttonTextStyle,
    this.borderRadius,
  });
}
